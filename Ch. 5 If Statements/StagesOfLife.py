age = 64

if age < 2:
    print(f"You are just a baby.")
elif age <= 3:
    print(f"You are just a toddler.")
elif age <= 12:
    print(f"You are just a kid.")
elif age <= 19:
    print(f"You are just a teenager.")
elif age <= 64:
    print(f"You are an adult.")
else:
    print(f"You are an elder.")